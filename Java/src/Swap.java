public class Swap {
    public static void main(String[] args){
        int a = 10;
        int b = 20;
        System.out.println("Before swap " + "a is " + a + " " + "b is "+ b);
        int temp ;
        temp = a;
        a = b;
        b = temp;
        System.out.println("After swap " + "a is " + a + " " + "b is "+ b);

    }


}
